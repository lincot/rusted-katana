pub fn special_primes(n: u32) -> [(u32, u32, u32); 3] {
    [
        BOUNCY_SPECIAL_PRIMES,
        INCREASING_SPECIAL_PRIMES,
        DECREASING_SPECIAL_PRIMES,
    ]
    .map(|primes| {
        let i = primes.partition_point(|&x| x <= n);
        if i == 0 {
            (0, 0, 0)
        } else {
            (primes[0], primes[i - 1], i as u32)
        }
    })
}

